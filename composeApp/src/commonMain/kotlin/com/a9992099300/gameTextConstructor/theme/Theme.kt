@Composable
internal fun AppTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit
) {
    if (!useDarkTheme) {
        CompositionLocalProvider(
            LocalColorProvider provides palette,
            content = content
        )
    } else {
        CompositionLocalProvider(
            LocalColorProvider provides palette,
            content = content
        )
    }
}
